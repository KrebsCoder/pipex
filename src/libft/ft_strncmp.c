#include "../pipex.h"

int	ft_strncmp(const char *s1, const char *s2, unsigned int n)
{
	unsigned char	*str1;
	unsigned char	*str2;
	size_t			i;

	if (n == 0)
		return (0);
	str1 = (unsigned char *)s1;
	str2 = (unsigned char *)s2;
	i = 0;
	while ((str1[i] == str2[i]) && str1[i] && str2[i] && i < n)
		i++;
	if (i < n)
		return (str1[i] - str2[i]);
	return (0);
}
